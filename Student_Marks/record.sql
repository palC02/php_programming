CREATE DATABASE MARKS;
use MARKS;

CREATE TABLE SCORESHEET(
    id INT(100) NOT NULL UNIQUE PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    t1 DECIMAL(100),
    t2 DECIMAL(100),
    t3 DECIMAL(100),
    t4 DECIMAL(100),
    p1 DECIMAL(100),
    p2 DECIMAL(100),
    total DECIMAL(10,2),
    average DECIMAL(10,2),
    division VARCHAR(20)
);


